package com.bipin.video.day7;

public class OperatorsDemo4 {
    public static void main(String[] args) {
        //Relational Operator
        // >, <,>=. <=,==,!=,
        //true or false
        int a=5;
        int b=6;
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);
        System.out.println(a==b);
        System.out.println(a!=b);
    }
}
